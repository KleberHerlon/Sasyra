   ROWS 1;

-- Batch fetch: assessment + related normalized data (1+N → 1 query via JOIN)
CREATE OR REPLACE FUNCTION sasyra_get_full_assessment(
  p_assessment_id BIGINT,
  p_user_id UUID
) RETURNS JSONB AS $$
DECLARE
  v_result JSONB;
BEGIN
  SELECT jsonb_build_object(
    'assessment', row_to_json(a)::jsonb,
    'muscle_tests', COALESCE((SELECT jsonb_agg(row_to_json(mt)::jsonb) FROM assessment_muscle_tests mt WHERE mt.assessment_id = a.id), '[]'::jsonb),
    'goniometry', COALESCE((SELECT jsonb_agg(row_to_json(g)::jsonb) FROM assessment_goniometry g WHERE g.assessment_id = a.id), '[]'::jsonb),
    'special_tests', COALESCE((SELECT jsonb_object_agg(st.test_name, st.result) FROM assessment_special_tests st WHERE st.assessment_id = a.id), '{}'::jsonb)
  ) INTO v_result
  FROM assessments a
  WHERE a.id = p_assessment_id AND a.user_id = p_user_id;

  RETURN v_result;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER
   STABLE;


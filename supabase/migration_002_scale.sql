    p_assessment->>'marcha', p_assessment->>'edema', p_assessment->>'palpacao',
    p_assessment->>'sensib', p_assessment->>'reflexos', p_assessment->>'obs',
    p_assessment->>'regiao', p_assessment->>'diagnosticoCinesio',
    COALESCE(p_assessment->'vitalSigns', '{}'::jsonb),
    p_assessment->>'impressaoClinica',
    COALESCE(p_assessment->'autoCIF', '[]'::jsonb),
    COALESCE(p_assessment->'recommendedScales', '[]'::jsonb),
    (p_assessment->>'honorario')::NUMERIC,
    COALESCE((p_assessment->>'isExpress')::BOOLEAN, FALSE),
    COALESCE(p_assessment->>'status', 'complete')
  ) RETURNING id INTO v_assessment_id;

  -- Insert normalized muscle tests
  FOR v_item IN SELECT * FROM jsonb_array_elements(p_muscle_tests)
  LOOP
    INSERT INTO assessment_muscle_tests (assessment_id, user_id, muscle, value)
    VALUES (v_assessment_id, p_user_id, v_item->>'muscle', (v_item->>'value')::NUMERIC);

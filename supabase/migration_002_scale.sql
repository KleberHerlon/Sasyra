-- 17. PAGINAÇÃO — função genérica para queries paginadas
-- ════════════════════════════════════════════════════════════
CREATE OR REPLACE FUNCTION sasyra_paginate(
  p_sql TEXT,
  p_page INT DEFAULT 1,
  p_per_page INT DEFAULT 50,
  p_max_per_page INT DEFAULT 200
) RETURNS JSONB AS $$
DECLARE
  v_offset INT;
  v_limit INT;
  v_total INT;
  v_rows JSONB;
BEGIN
  v_limit := LEAST(p_per_page, p_max_per_page);
  v_offset := (GREATEST(p_page, 1) - 1) * v_limit;

  -- Count total (expects the SQL to work with COUNT(*))
  EXECUTE 'SELECT COUNT(*) FROM (' || p_sql || ') AS _count' INTO v_total;

  -- Fetch paginated rows
  EXECUTE 'SELECT COALESCE(jsonb_agg(row_to_json(t)), ''[]''::jsonb) FROM (' ||
    p_sql || ' LIMIT ' || v_limit || ' OFFSET ' || v_offset || ') AS t'
  INTO v_rows;

  RETURN jsonb_build_object(
    'data', v_rows,
    'total', v_total,
    'page', p_page,
    'per_page', v_limit,
    'total_pages', CEIL(v_total::NUMERIC / v_limit)::INT
  );
END;
$$ LANGUAGE plpgsql SECURITY DEFINER
   STABLE;


-- 4. PATIENTS — com índices compostos para busca textual
-- ════════════════════════════════════════════════════════════
CREATE TABLE IF NOT EXISTS patients (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  nome TEXT NOT NULL,
  data_nasc TEXT,
  sexo TEXT,
  lateralidade TEXT,
  estado_civil TEXT,
  profissao TEXT,
  convenio TEXT,
  sessoes_auth TEXT,
  telefone TEXT,
  peso TEXT,
  altura TEXT,
  data_cadastro TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  has_express BOOLEAN DEFAULT FALSE,
  express_date TEXT,
  meta_data JSONB DEFAULT '{}'::jsonb,
  -- busca textual (preenchido por trigger)
  search_vector TSVECTOR GENERATED ALWAYS AS (
    to_tsvector('portuguese', coalesce(nome,'') || ' ' || coalesce(telefone,'') || ' ' || coalesce(profissao,'') || ' ' || coalesce(convenio,''))
  ) STORED
);
CREATE INDEX IF NOT EXISTS idx_patients_user_id ON patients(user_id);
CREATE INDEX IF NOT EXISTS idx_patients_user_nome ON patients(user_id, nome);
CREATE INDEX IF NOT EXISTS idx_patients_search ON patients USING GIN(search_vector);
ALTER TABLE patients ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS "Users can CRUD own patients" ON patients;
CREATE POLICY "Users can CRUD own patients" ON patients FOR ALL USING (auth.uid() = user_id);


  honorario NUMERIC,
  is_express BOOLEAN DEFAULT FALSE,
  status TEXT DEFAULT 'complete',
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  PRIMARY KEY (id, user_id)
) PARTITION BY HASH (user_id);

-- 16 partições (escolha baseada em ~16 vCPUs no Supabase)
DO $$ BEGIN
  FOR i IN 0..15 LOOP
    EXECUTE format(
      'CREATE TABLE IF NOT EXISTS assessments_p%02d PARTITION OF assessments FOR VALUES WITH (MODULUS 16, REMAINDER %s)',
      i, i
    );
  END LOOP;
END $$;

-- Índices locais em cada partição (herdados automaticamente)
CREATE INDEX IF NOT EXISTS idx_assessments_patient_data ON assessments(patient_id, data DESC);
CREATE INDEX IF NOT EXISTS idx_assessments_user_data ON assessments(user_id, data DESC);
CREATE INDEX IF NOT EXISTS idx_assessments_created_at ON assessments(created_at DESC);

ALTER TABLE assessments ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS "Users can CRUD own assessments" ON assessments;
CREATE POLICY "Users can CRUD own assessments" ON assessments FOR ALL USING (auth.uid() = user_id);


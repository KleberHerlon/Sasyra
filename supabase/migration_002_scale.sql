-- 20. MIGRAÇÃO DE DADOS: localStorage → Supabase
-- ════════════════════════════════════════════════════════════
CREATE OR REPLACE FUNCTION sasyra_migrate_from_localstorage(p_user_id UUID, p_data JSONB)
RETURNS JSONB AS $$
DECLARE
  v_result JSONB;
  v_patients INT := 0;
  v_assessments INT := 0;
  v_logs INT := 0;
  v_errors JSONB := '[]'::jsonb;
  v_item JSONB;
BEGIN
  -- Migrate patients
  IF p_data ? 'patients' THEN
    FOR v_item IN SELECT * FROM jsonb_array_elements(p_data->'patients')
    LOOP
      BEGIN
        INSERT INTO patients (user_id, nome, data_nasc, sexo, lateralidade, estado_civil, profissao, convenio, sessoes_auth, telefone, peso, altura, has_express, express_date)
        VALUES (p_user_id, v_item->>'nome', v_item->>'dataNasc', v_item->>'sexo', v_item->>'lateralidade', v_item->>'estadoCivil', v_item->>'profissao', v_item->>'convenio', v_item->>'sessoesAuth', v_item->>'telefone', v_item->>'peso', v_item->>'altura', COALESCE((v_item->>'hasExpress')::BOOLEAN, FALSE), v_item->>'expressDate');
        v_patients := v_patients + 1;
      EXCEPTION WHEN OTHERS THEN
        v_errors := v_errors || jsonb_build_object('type', 'patient', 'nome', v_item->>'nome', 'error', SQLERRM);
      END;
    END LOOP;
  END IF;

  -- More migrations can be added here (assessments, logs, etc.)

  v_result := jsonb_build_object(
    'patients_migrated', v_patients,
    'assessments_migrated', v_assessments,
    'logs_migrated', v_logs,
    'errors', v_errors
  );
  RETURN v_result;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;


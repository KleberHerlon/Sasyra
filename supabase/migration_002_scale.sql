CREATE TABLE IF NOT EXISTS sasyra_cache (
  cache_key TEXT PRIMARY KEY,
  value JSONB NOT NULL,
  expires_at TIMESTAMPTZ NOT NULL DEFAULT (NOW() + INTERVAL '5 minutes'),
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);
CREATE INDEX IF NOT EXISTS idx_cache_expires ON sasyra_cache(expires_at);

-- Função de leitura com cache
CREATE OR REPLACE FUNCTION sasyra_cache_get(p_key TEXT, p_ttl_seconds INT DEFAULT 300)
RETURNS JSONB AS $$
DECLARE
  v_value JSONB;
BEGIN
  SELECT value INTO v_value FROM sasyra_cache
    WHERE cache_key = p_key AND expires_at > NOW();
  RETURN v_value;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Função de escrita no cache
CREATE OR REPLACE FUNCTION sasyra_cache_set(p_key TEXT, p_value JSONB, p_ttl_seconds INT DEFAULT 300)
RETURNS VOID AS $$
BEGIN
  INSERT INTO sasyra_cache (cache_key, value, expires_at)
  VALUES (p_key, p_value, NOW() + (p_ttl_seconds || ' seconds')::INTERVAL)
  ON CONFLICT (cache_key) DO UPDATE SET
    value = EXCLUDED.value,
    expires_at = EXCLUDED.expires_at;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Job de limpeza do cache (pode ser chamado por pg_cron ou manualmente)
CREATE OR REPLACE FUNCTION sasyra_cache_cleanup()
RETURNS INT AS $$
DECLARE
  v_deleted INT;
BEGIN
  DELETE FROM sasyra_cache WHERE expires_at < NOW();
  GET DIAGNOSTICS v_deleted = ROW_COUNT;
  RETURN v_deleted;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;


  piora JSONB DEFAULT '[]'::jsonb,
  hda TEXT,
  comorbid JSONB DEFAULT '[]'::jsonb,
  antec JSONB DEFAULT '[]'::jsonb,
  meds TEXT,
  yellow_flags JSONB DEFAULT '[]'::jsonb,
  selected_red_flags JSONB DEFAULT '[]'::jsonb,
  eva_mov NUMERIC,
  eva_rep NUMERIC,
  avds JSONB DEFAULT '[]'::jsonb,
  obj_trat JSONB DEFAULT '[]'::jsonb,
  nivel_ati TEXT,

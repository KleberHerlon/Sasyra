-- 0. ENUMs
CREATE TYPE plan_type AS ENUM ('start', 'evidencia', 'clinicas', 'ia');
CREATE TYPE billing_type AS ENUM ('monthly', 'yearly');
CREATE TYPE module_type AS ENUM ('fisioterapia', 'terapiaOcupacional', 'nutricao', 'educacaoFisica', 'pediatria', 'crossfit', 'neuro');
CREATE TYPE appointment_type AS ENUM ('sessao', 'avaliacao', 'bloqueio');
CREATE TYPE appointment_status AS ENUM ('agendado', 'confirmado', 'concluido', 'faltou', 'cancelado');

-- 1. PROFILES (extends auth.users)
CREATE TABLE profiles (
  id UUID PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
  nome TEXT NOT NULL,
  email TEXT,
  prof TEXT NOT NULL,
  crefito TEXT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);
ALTER TABLE profiles ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Users can read own profile" ON profiles FOR SELECT USING (auth.uid() = id);
CREATE POLICY "Users can update own profile" ON profiles FOR UPDATE USING (auth.uid() = id);

-- 2. PATIENTS
CREATE TABLE patients (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  nome TEXT NOT NULL,
  data_nasc TEXT,
  sexo TEXT,
  lateralidade TEXT,
  estado_civil TEXT,
  profissao TEXT,
  convenio TEXT,
  sessoes_auth TEXT,
  telefone TEXT,
  peso TEXT,
  altura TEXT,
  data_cadastro TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  has_express BOOLEAN DEFAULT FALSE,
  express_date TEXT,
  meta_data JSONB DEFAULT '{}'::jsonb
);
CREATE INDEX idx_patients_user_id ON patients(user_id);
ALTER TABLE patients ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Users can CRUD own patients" ON patients FOR ALL USING (auth.uid() = user_id);


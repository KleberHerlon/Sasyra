  resposta TEXT,
  evolucao TEXT,
  metas TEXT,
  escalas TEXT,
  escala_data JSONB DEFAULT '{}'::jsonb,
  pa TEXT,
  adms JSONB DEFAULT '[]'::jsonb,
  mrcs JSONB DEFAULT '[]'::jsonb,
  sessao_num INTEGER,
  spo2 TEXT,
  glucose TEXT,
  heart_rate TEXT,
  is_express_vital BOOLEAN DEFAULT FALSE,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
